// create a structure to store complex numbers. (use arrow operator)
#include <stdio.h>

struct complex {
    int real;
    int img;
};

int main () {
    struct complex number1 = {5, 8}; // number1.real = 5, number1.imaginary = 8
    struct complex *ptr = &number1;

    printf("real part = %d\n", ptr->real);
    printf("img part = %d\n\n", ptr->img);

    return 0;
}